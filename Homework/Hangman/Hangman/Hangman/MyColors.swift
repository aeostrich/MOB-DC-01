import UIKit

class MyColors {
    class func red() -> UIColor {
        return UIColor(red: 225/256, green: 125/256, blue: 130/256, alpha: 1.0)
    }
    
    class func green() -> UIColor {
        return UIColor(red: 155/256, green: 210/256, blue: 200/256, alpha: 1.0)
    }
    
    class func blue() -> UIColor {
        return UIColor(red: 80/256, green: 150/256, blue: 210/256, alpha: 1.0)
    }
    
}
